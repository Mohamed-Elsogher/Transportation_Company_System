    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        // TODO add your handling code here:
        String AddVehicelType = txtVehcileType.getText();
        String VehicleCapacity = txtVehicleCapacity.getText();
        String LicensePlate = txtlicense.getText();
     
        if (VehicleCapacity.isEmpty() || LicensePlate.isEmpty() || AddVehicelType.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                                    "Please enter all fields",
                                    "Try again",
                                    JOptionPane.ERROR_MESSAGE);
        } else {
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.addRow(new Object[]{AddVehicelType, VehicleCapacity,LicensePlate});
            
            writeVehicleToFile(AddVehicelType, VehicleCapacity, LicensePlate);
            txtVehcileType.setText("");
           txtVehicleCapacity.setText("");
             txtlicense.setText("");
            
        }
    }//GEN-LAST:event_btAddActionPerformed
private void writeVehicleToFile(String Type, String Capacity , String LicensePlate) {
        String fileName = "Vehicle.txt";
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(fileName, true))) {
            
           writer.write( Type +","+" "+Capacity +","+" "+ LicensePlate +"\n");
            JOptionPane.showMessageDialog(null, "Vehicle added successfully.");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "An error occurred : " + e.getMessage());
        }
    }
